def detect_git_url(repo_path: Path) -> Optional[str]:
    """Detect the git remote URL for a local repo."""
    try:
        result = subprocess.run(
            ["git", "remote", "get-url", "origin"],
            cwd=str(repo_path),
            capture_output=True,
            text=True,
            timeout=5,
        )
        if result.returncode == 0:
            url = result.stdout.strip()
            # Convert SSH to HTTPS for OCI
            if url.startswith("git@github.com:"):
                url = url.replace("git@github.com:", "https://github.com/").removesuffix(".git") + ".git"
            return url
    except Exception as e:
        logger.debug("Could not detect git URL: %s", e)
    return None


def detect_default_branch(repo_path: Path) -> str:
    """Detect the current branch of a local repo."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--abbrev-ref", "HEAD"],
            cwd=str(repo_path),
            capture_output=True,
            text=True,
            timeout=5,
        )
        if result.returncode == 0:
            return result.stdout.strip() or "main"
    except Exception:
        pass
    return "main"


# ---------------------------------------------------------------------------
# Repo ID persistence -- .saar/config.json
# ---------------------------------------------------------------------------

def save_repo_id(repo_path: Path, repo_id: str) -> None:
    """Save OCI repo_id to .saar/config.json for future use by saar diff / MCP."""
    cache_file = repo_path / ".saar" / "config.json"
    cache_dir = cache_file.parent
    cache_dir.mkdir(exist_ok=True)

    data: dict = {}
    if cache_file.exists():
        try:
            data = json.loads(cache_file.read_text(encoding="utf-8"))
        except Exception:
            pass

    data["oci_repo_id"] = repo_id
    cache_file.write_text(json.dumps(data, indent=2), encoding="utf-8")
    logger.debug("Saved OCI repo_id %s to %s", repo_id, cache_file)


def load_repo_id(repo_path: Path) -> Optional[str]:
    """Load a previously saved OCI repo_id for this repo."""
    cache_file = repo_path / ".saar" / "config.json"
    if not cache_file.exists():
        return None
    try:
        data = json.loads(cache_file.read_text(encoding="utf-8"))
        return data.get("oci_repo_id")
    except Exception:
        return None
